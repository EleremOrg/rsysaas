enum ShoeSize {
    Eu(f32),
    Us(f32),
    Uk(f32),
}
enum DeckWidth {
    Under7_5,
    Between7_5And7_875,
    Between7_625And8_0,
    Between7_75And8_0,
    Between7_75And8_125,
    Between7_875And8_125,
    Between7_875And8_25,
    Between8_0And8_25,
    Between8_0And8_375,
    Between8_125And8_5,
    Between8_25And8_5,
    Between8_25And8_625,
    Between8_375And8_625,
    Between8_5And8_625,
    Over8_5,
}

fn recommend_deck_size(size: ShoeSize) -> DeckWidth {
    match size {
        ShoeSize::Us(us_size) => match us_size {
            s if s < 4.0 => DeckWidth::Under7_5,
            4.0..=4.5 => DeckWidth::Between7_5And7_875,
            5.0..=5.5 => DeckWidth::Between7_625And8_0,
            6.0 => DeckWidth::Between7_75And8_0,
            6.5 => DeckWidth::Between7_75And8_125,
            7.0 => DeckWidth::Between7_875And8_125,
            7.5 => DeckWidth::Between7_875And8_25,
            8.0..=8.5 => DeckWidth::Between8_0And8_25,
            9.0..=9.5 => DeckWidth::Between8_0And8_375,
            10.0..=10.5 => DeckWidth::Between8_125And8_5,
            11.0 => DeckWidth::Between8_25And8_5,
            11.5 => DeckWidth::Between8_25And8_625,
            12.0 => DeckWidth::Between8_375And8_625,
            13.0 => DeckWidth::Between8_5And8_625,
            _ => DeckWidth::Over8_5,
        },
        ShoeSize::Eu(eu_size) => match eu_size {
            s if s < 36.0 => DeckWidth::Under7_5,
            36.0..=36.5 => DeckWidth::Between7_5And7_875,
            37.5..=38.0 => DeckWidth::Between7_625And8_0,
            38.5 => DeckWidth::Between7_75And8_0,
            39.0 => DeckWidth::Between7_75And8_125,
            40.0 => DeckWidth::Between7_875And8_125,
            40.5 => DeckWidth::Between7_875And8_25,
            41.0..=42.0 => DeckWidth::Between8_0And8_25,
            42.5..=43.0 => DeckWidth::Between8_0And8_375,
            44.0..=44.5 => DeckWidth::Between8_125And8_5,
            45.0 => DeckWidth::Between8_25And8_5,
            45.5 => DeckWidth::Between8_25And8_625,
            46.0 => DeckWidth::Between8_375And8_625,
            47.5 => DeckWidth::Between8_5And8_625,
            _ => DeckWidth::Over8_5,
        },
        ShoeSize::Uk(uk_size) => match uk_size {
            s if s < 3.5 => DeckWidth::Under7_5,
            3.5 => DeckWidth::Between7_5And7_875,
            4.0..=4.5 => DeckWidth::Between7_625And8_0,
            5.0 => DeckWidth::Between7_75And8_0,
            5.5 => DeckWidth::Between7_75And8_125,
            6.0 => DeckWidth::Between7_875And8_125,
            6.5 => DeckWidth::Between7_875And8_25,
            7.0..=7.5 => DeckWidth::Between8_0And8_25,
            8.0..=8.5 => DeckWidth::Between8_0And8_375,
            9.0..=9.5 => DeckWidth::Between8_125And8_5,
            10.0 => DeckWidth::Between8_25And8_5,
            10.5 => DeckWidth::Between8_25And8_625,
            11.0 => DeckWidth::Between8_375And8_625,
            12.0 => DeckWidth::Between8_5And8_625,
            _ => DeckWidth::Over8_5,
        },
    }
}
